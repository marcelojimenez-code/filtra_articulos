class RopaAlmacen {
    constructor(ropa) {
        this.ropa = ropa;
    }

    filtrar(criterio, valor) {

        const resultado = this.ropa.filter(item => {
            if (criterio === 'marca') {
                return item.marca === valor;
            } else if (criterio === 'precio') {
                return item.precio >= Number(valor);
            } else if (criterio === 'tipo') {
                return item.tipo.toLowerCase() === valor.toLowerCase();
            } else if (criterio === 'talla') {
                return item.talla === valor.toUpperCase();
            } else if (criterio === 'sexo'){
                return item.sexo === valor.toLowerCase();
            } else {
                return item;
            }
        })

        if (resultado.length > 0) {
            return resultado;                                      
        } else {
            alert("No se encontraron ropas con esas caracteristicas");
        }
    }

    salida(criterio, valor){
        const resultadoFiltrado = this.filtrar(criterio, valor);
        const resultDiv = document.getElementById("resultado");
                
        resultDiv.innerHTML = "<ul>";
            resultadoFiltrado.forEach(item => {
                resultDiv.innerHTML += `<li>${item.tipo} - ${item.precio}- ${item.cantidad}- ${item.talla}- ${item.sexo}- ${item.marca}</li>`;
            });
            resultDiv.innerHTML += "</ul>";
        
    }
}

